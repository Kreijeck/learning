#include <iostream>


int main()
{
    double number1 = 10.0;
    double number2 = 2.0;
    double result;

    // Multiplikation
    result = number1 * number2;
    std::cout << "number1 * number2 = " << result << std::endl;

    // Addition
    result = number1 + number2;
    std::cout << "number1 + number2 = " << result << std::endl;

    //Subtraktion
    result = number1 - number2;
    std::cout << "number1 - number2 = " << result << std::endl;

    //Division
    result = number1 / number2;
    std::cout << "number1 / number2 = " << result << std::endl;

    return 0;
}
